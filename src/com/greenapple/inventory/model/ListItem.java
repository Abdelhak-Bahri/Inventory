package com.greenapple.inventory.model;

public class ListItem {
	
	private int num;
	private String productOrfile;
	private String numberOrfile;
	public String getProductOrfile() {
		return productOrfile;
	}
	public void setProductOrfile(String productOrfile) {
		this.productOrfile = productOrfile;
	}
	public String getNumberOrfile() {
		return numberOrfile;
	}
	public void setNumberOrfile(String numberOrfile) {
		this.numberOrfile = numberOrfile;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	

}
